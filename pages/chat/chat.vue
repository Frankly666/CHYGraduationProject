          messageList.value = [...currentSession.value.messages];
          
          // 滚动到底部 - 每次内容更新都进行滚动
          scrollToBottom();
        },
        historyMessages
      );
      
      console.log('文件问答完成');
      
      // 保存助手消息到数据库，只有在没有错误的情况下才保存
      if (currentSession.value.id && !currentSession.value.messages[assistantMessageIndex].isError) {
        await createMessage(
          currentSession.value.id,
          response,
          'assistant'
        );
      }
    } else {
