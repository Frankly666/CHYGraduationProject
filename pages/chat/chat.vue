// 发送消息
const sendMessage = async (contentToResend = null, isRegenerating = false) => {
  const currentInput = inputMessage.value.trim();
  const messageContent = isRegenerating ? contentToResend : currentInput;

  if (!messageContent && !isFileChat && !isRegenerating) {
    // 如果不是重新生成，并且输入为空（且不是文件聊天），则不发送
    return;
  }
  
  // 即使是重新生成，也检查canSend，但要排除inputMessage.value.trim()的判断，因为此时依赖contentToResend
  if (!isRegenerating && !canSend.value) return;
  
  // 不再阻止用户在生成知识图谱时发送消息
  
  // 如果不是重新生成，则添加用户消息到列表并清空输入框
  if (!isRegenerating) {
    currentSession.value.messages.push({
      role: 'user',
      content: messageContent, // 使用 messageContent
      time: Date.now()
    });
    inputMessage.value = ''; // 清空输入框
  } else {
    // 如果是重新生成，我们不再次添加用户消息，因为它是基于历史消息的
    // 但我们可能仍想清空输入框，以防用户在重新生成时输入了什么
    inputMessage.value = ''; 
  }
  
  // 更新UI显示的消息列表，无论是否重新生成，都需要更新以反映思考状态等
  messageList.value = [...currentSession.value.messages]; 
  const userMessageForRequest = messageContent; // 用于发送请求的内容
  
  // 如果是第一条消息，先生成标题
  if (!currentSession.value.id && isLoggedIn.value) {
    try {
      console.log('首次发送消息，生成标题并创建新会话');
      // 生成标题
      const generatedTitle = await generateTitle(userMessageForRequest);
      console.log('生成的标题:', generatedTitle);
      currentSession.value.title = generatedTitle;
      
      // 创建新会话
      const sessionResult = await createSession(currentSession.value.title, currentSession.value.type);
      console.log('创建会话结果:', sessionResult);
      
      // 检查会话ID，云函数返回的ID在_id字段中
      if (!sessionResult || (!sessionResult._id && !sessionResult.id)) {
        throw new Error('创建会话失败：未获取到会话ID');
      }
      
      // 获取会话ID，优先使用_id字段，兼容id字段
      const sessionId = sessionResult._id || sessionResult.id;
      currentSession.value.id = sessionId;
      console.log('成功创建会话，ID:', sessionId);
    } catch (error) {
      console.error('创建会话失败:', error);
      uni.showToast({
        title: error.message || '创建会话失败',
        icon: 'none'
      });
      // 即使创建会话失败，也继续处理消息，只是不会保存到数据库
    }
  }
  
  // 如果不是重新生成，并且会话存在，则保存用户消息到数据库
  if (!isRegenerating && currentSession.value.id) {
    await createMessage(
      currentSession.value.id,
      userMessageForRequest, // 使用 userMessageForRequest
      'user'
    );
  }
  
  // 添加助手消息占位
  const assistantMessageIndex = currentSession.value.messages.length;
  currentSession.value.messages.push({
    role: 'assistant',
    content: '',
    thinking: true,
    time: Date.now() // AI消息的时间戳应该在它实际开始生成时或完成后更新，这里是占位符的初始时间
  });
  
  // 更新消息列表以显示思考状态
  messageList.value = [...currentSession.value.messages];
  
  try {
    let response;
    if (isFileChat.value && currentFile.value) {
      console.log('开始文件问答:', {
        question: userMessageForRequest, // 使用 userMessageForRequest
        fileContent: currentFile.value.content
      });
      
      // 准备历史消息，过滤掉空内容和系统消息
      const historyMessages = currentSession.value.messages
        .filter(msg => 
          msg.role !== 'system' && 
          msg.role !== 'thinking' && 
          msg.content && 
          msg.content.trim() !== '' &&
          !msg.thinking
        )
        .map(msg => ({
          role: msg.role,
          content: msg.content
        }));
      
      console.log('文件问答历史消息:', historyMessages);
      
      // 确保文件信息完整
      if (!currentFile.value || !currentFile.value.content) {
        throw new Error('文件信息不完整，请重新上传文件');
      }
      
      // 调用文件问答接口，传入进度回调函数和历史消息
      response = await chatWithFile(
        currentFile.value.content, 
        userMessageForRequest, // 使用 userMessageForRequest
        (chunk, fullResponse) => {
          // 更新助手消息内容
          currentSession.value.messages[assistantMessageIndex] = {
            role: 'assistant',
            content: fullResponse,
            thinking: false,
            time: Date.now()
          };
          
